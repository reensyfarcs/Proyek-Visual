    private void usnFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_usnFocusGained
       String usser= usn.getText();
       if(usser.equals("Username")){
           usn.setText("");
       }
    }//GEN-LAST:event_usnFocusGained

    private void usnFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_usnFocusLost
      String usser=usn.getText();
      if(usser.equals("")||usser.equals("Username")){
          usn.setText("Username");
      }
    }//GEN-LAST:event_usnFocusLost

    private void lblusnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblusnMouseClicked
        // TODO add your handling code here:
        try{
            String username = usn.getText();
            String password = new String(pw.getPassword());
            try (FileWriter fw = new FileWriter("remember.txt")) {
                fw.write(username + "\n");
                fw.write(password);
                fw.close();
            }
            JOptionPane.showMessageDialog(this, "Username & Password Disimpan!");
        } catch (IOException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_lblusnMouseClicked

    private void pwFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_pwFocusGained
        // TODO add your handling code here:
        String pass = new String(pw.getPassword());
        if (pass.equals("Password")){
            pw.setText("");
            pw.setEchoChar('•');
        }
    }//GEN-LAST:event_pwFocusGained

    private void pwFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_pwFocusLost
        // TODO add your handling code here:
        String pass = new String(pw.getPassword());
        if(pass.equals("")||pass.equals("Password")){
            pw.setText("Password");
            pw.setEchoChar((char)0);
        }
    }//GEN-LAST:event_pwFocusLost

    private void lblpwMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblpwMouseClicked
        // TODO add your handling code here:
        if (isPasswordVisible){
            pw.setEchoChar('•');
            lblpw.setIcon(new javax.swing.ImageIcon(getClass().getResource("/img_icon/eye.png")));
            isPasswordVisible = false;
        }else {
            pw.setEchoChar((char)0);
            lblpw.setIcon(new javax.swing.ImageIcon(getClass().getResource("/img_icon/view.png")));
            isPasswordVisible = true;
        }
    }//GEN-LAST:event_lblpwMouseClicked

